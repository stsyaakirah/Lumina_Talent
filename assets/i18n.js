  t(key) {
    return this.messages[this.currentLang][key] || this.messages['id'][key] || key;
  },

  setLang(lang) {
    this.currentLang = lang;
    localStorage.setItem('lumina_lang', lang);
    this.applyTranslations();
    document.querySelectorAll('.lang-btn').forEach(b => {
      b.classList.toggle('active', b.dataset.lang === lang);
    });
  },

  applyTranslations() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.dataset.i18n;
      const val = this.t(key);
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = val;
      } else {
        el.textContent = val;
      }
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      el.placeholder = this.t(el.dataset.i18nPlaceholder);
    });
  },

  init() {
    this.applyTranslations();
    document.querySelectorAll('.lang-btn').forEach(b => {
      b.classList.toggle('active', b.dataset.lang === this.currentLang);
      b.addEventListener('click', () => this.setLang(b.dataset.lang));
    });
  }
